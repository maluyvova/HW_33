package core;
public class Calculator{
	public static  double add ( double a , double b ) {return a+b;}
	public static double add ( double a, double b, double c ) {return a+b+c;}
	public static double add (double a , double b, double c , double d ) {return a+b+c+d;}
	public static double divide (double a, double b) {return a/b;}
	public static double divide (double a, double b, double c ) {return a/b/c;}
	public static double divide (double a , double b , double c , double d ) {return a/b/c/d;}
	public static double multiply (double a , double b) {return a*b;}
	public static double multiply (double a , double b, double c ) {return a*b*c;}
	public static double multiply (double a , double b , double c , double d) {return a*b*c*d;}
	public static double substruct (double a, double b) {return a -b;}
	public static double substruct (double a , double b , double c ) {return a - b -c ;}
	public static double substruct (double a , double b, double c , double d) {return a - b - c -d;}
	
	
	
	
}
	

